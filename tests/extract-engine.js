// Extracts the pure engine functions from bank-of-dad-app.html into a
// standalone CommonJS module (engine.gen.js) so they can be unit- and
// mutation-tested in isolation. Kept in sync by re-running this script.
const fs = require('fs');
const path = require('path');

// Look for the app HTML relative to this script first (archive layout:
