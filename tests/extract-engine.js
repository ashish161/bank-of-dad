function sliceBalanced(src, startIdx) {
  // Find first '{' at/after startIdx, then balance braces.
  let i = src.indexOf('{', startIdx);
  let depth = 0;
  for (; i < src.length; i++) {
    if (src[i] === '{') depth++;
    else if (src[i] === '}') {
      depth--;
      if (depth === 0) {
        // for const {...} we also need the trailing ';'
        let end = i + 1;
        if (src[end] === ';') end++;
        return end;
      }
    }
  }
  throw new Error('unbalanced');
}

let out = '// AUTO-GENERATED by extract-engine.js — do not edit by hand.\n';
out += '// Mutable state so functions that read settings can be driven by tests.\n';
out += "const MS_DAY = 24 * 3600 * 1000;\n";
out += "let state = { settings: { currency: '₹', base: 100, rate: 10, frequency: 'daily' } };\n";
out += 'function __setState(s){ state = s; }\n\n';

for (const w of WANTED) {
  let re;
  if (w.type === 'func') re = new RegExp('function\\s+' + w.name + '\\s*\\(');
  else re = new RegExp('const\\s+' + w.name + '\\s*=');
  const m = script.match(re);
  if (!m) { console.error('NOT FOUND:', w.name); process.exit(1); }
  const start = m.index;
  let end;
  if (w.type === 'const') {
    // Peek at what follows '=' : an object '{...}' balances braces; anything
    // else (array, literal) ends at the first ';' at depth 0.
    const eq = script.indexOf('=', start);
    const after = script.slice(eq + 1).replace(/^\s+/, '')[0];
    if (after === '{') {
      end = sliceBalanced(script, start);
    } else {
      end = script.indexOf(';', start) + 1;
    }
  } else {
    end = sliceBalanced(script, start);
  }
  out += script.slice(start, end).trim() + '\n\n';
}

// Export everything
out += 'module.exports = { __setState, get state(){return state;}, MS_DAY,\n';
out += '  ' + WANTED.map(w => w.name).join(', ') + '\n};\n';

fs.writeFileSync(path.join(__dirname, 'engine.gen.js'), out);
console.log('Wrote engine.gen.js (' + out.length + ' bytes, ' + WANTED.length + ' symbols)');
